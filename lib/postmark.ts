import { ServerClient } from 'postmark';
import { env } from '@/lib/env';
import { Log } from '@/server/db';

export const postmark = new ServerClient(
  env.POSTMARK_API_TOKEN || 'dummy-token'
);

export interface EmailAttachment {
  name: string;
  content: string; // Base64 encoded content
  contentType: string;
}

export const sendEmail = async (params: {
  to: string;
  subject: string;
  htmlBody: string;
  textBody?: string;
  attachments?: EmailAttachment[];
}) => {
  if (!env.POSTMARK_API_TOKEN) {
    console.log('Postmark not configured, skipping email send');
    return { MessageID: 'mock-message-id', ErrorCode: 0 };
  }

  // Use a verified domain for Postmark
  // Update this to match your verified domain in Postmark
  const fromAddress = 'admin@tanklog.co';

  try {
    console.log('Sending email via Postmark:', {
      from: fromAddress,
      to: params.to,
      subject: params.subject,
      hasAttachments: !!params.attachments?.length,
    });

    const response = await postmark.sendEmail({
      From: fromAddress,
      To: params.to,
      Subject: params.subject,
      HtmlBody: params.htmlBody,
      TextBody: params.textBody,
      Attachments: params.attachments?.map((att) => ({
        Name: att.name,
        Content: att.content,
        ContentType: att.contentType,
        ContentID: null,
      })),
    });

    console.log('Email sent successfully:', response);
    return response;
  } catch (error) {
    console.error('Failed to send email:', error);

    // If domain restriction error, log it and return a mock response
    if (
      error &&
      typeof error === 'object' &&
      'code' in error &&
      error.code === 412
    ) {
      console.log(
        'Postmark account pending approval - email would be sent to:',
        params.to
      );
      console.log(
        'Domain restriction: From address must match recipient domain'
      );
      return { MessageID: 'mock-message-id', ErrorCode: 0 };
    }

    throw error;
  }
};

export const sendTemplateEmail = async (params: {
  to: string;
  templateId: number;
  templateModel: Record<string, unknown>;
}) => {
  if (!env.POSTMARK_API_TOKEN) {
    console.log('Postmark not configured, skipping template email send');
    return { MessageID: 'mock-message-id', ErrorCode: 0 };
  }

  const response = await postmark.sendEmailWithTemplate({
    From: 'noreply@tanklog.app',
    To: params.to,
    TemplateId: params.templateId,
    TemplateModel: params.templateModel,
  });

  return response;
};

/**
 * Send log PDF via email
 */
export const sendLogPdfEmail = async (
  to: string,
  logData: Log,
  pdfBuffer: Buffer,
  pdfFilename: string
) => {
  console.log('=== SEND EMAIL DEBUG START ===');
  console.log('To:', to);
  console.log('Log ID:', logData.id);
  console.log('PDF buffer size:', pdfBuffer.length);
  console.log('PDF filename:', pdfFilename);

  const subject = `TankLog Inspection Report - ${logData.site || logData.tank_id} - ${new Date(logData.occurred_at).toLocaleDateString()}`;
  console.log('Subject:', subject);

  // Clean notes by removing signature data
  const cleanNotes = logData.notes
    ? logData.notes
        .replace(/\[SIGNATURE_DATA\][\s\S]*?\[\/SIGNATURE_DATA\]/g, '')
        .trim()
    : '';

  const htmlBody = `
    <div style="font-family: Arial, sans-serif; max-width: 600px; margin: 0 auto;">
      <h2 style="color: #0066cc;">TankLog Inspection Report</h2>
      
      <p>Hello,</p>
      
      <p>Your inspection report has been generated and is attached below. This professional compliance report includes all inspection details, regulatory context, and actionable insights.</p>
      
      <div style="background: #f8f9fa; padding: 15px; border-radius: 8px; margin: 20px 0;">
        <h3 style="color: #0066cc; margin-top: 0;">Inspection Summary</h3>
        <ul style="margin: 0;">
          <li><strong>Site:</strong> ${logData.site || 'N/A'}</li>
          <li><strong>Tank ID:</strong> ${logData.tank_id}</li>
          <li><strong>Date:</strong> ${new Date(logData.occurred_at).toLocaleDateString()}</li>
          <li><strong>Leak Check:</strong> ${logData.leak_check ? '✅ Pass' : '❌ Fail'}</li>
          ${logData.visual_ok !== null ? `<li><strong>Visual Inspection:</strong> ${logData.visual_ok ? '✅ All OK' : '❌ Issues Found'}</li>` : ''}
        </ul>
      </div>
      
      ${cleanNotes ? `<p><strong>Notes:</strong> ${cleanNotes}</p>` : ''}
      
      <p>This report has been automatically generated by TankLog and saved to your account. The attached PDF contains detailed compliance information, regulatory context, and maintenance recommendations.</p>
      
      <hr style="margin: 20px 0; border: none; border-top: 1px solid #e5e7eb;">
      
      <p style="font-size: 12px; color: #6b7280;">
        Generated by TankLog • ${new Date().toISOString()}
      </p>
    </div>
  `;

  const textBody = `
TankLog Inspection Report

Your inspection report has been generated and is attached below. This professional compliance report includes all inspection details, regulatory context, and actionable insights.

INSPECTION SUMMARY:
Site: ${logData.site || 'N/A'}
Tank ID: ${logData.tank_id}
Date: ${new Date(logData.occurred_at).toLocaleDateString()}
Leak Check: ${logData.leak_check ? 'Pass' : 'Fail'}
${logData.visual_ok !== null ? `Visual Inspection: ${logData.visual_ok ? 'All OK' : 'Issues Found'}` : ''}

${cleanNotes ? `Notes: ${cleanNotes}` : ''}

This report has been automatically generated by TankLog and saved to your account. The attached PDF contains detailed compliance information, regulatory context, and maintenance recommendations.

Generated by TankLog • ${new Date().toISOString()}
  `;

  // Convert PDF to base64 (pdfBuffer is Uint8Array from Puppeteer)
  const base64Content = Buffer.from(pdfBuffer).toString('base64');
  // Validate base64 content
  const base64Regex = /^[A-Za-z0-9+/]*={0,2}$/;
  if (!base64Regex.test(base64Content)) {
    throw new Error('Invalid base64 content generated');
  }

  console.log('Calling sendEmail with attachments...');
  const result = await sendEmail({
    to,
    subject,
    htmlBody,
    textBody,
    attachments: [
      {
        name: pdfFilename,
        content: base64Content,
        contentType: 'application/pdf',
      },
    ],
  });
  console.log('=== SEND EMAIL DEBUG END ===');
  console.log('Email result:', result);
  return result;
};

